create table UTILIZATOR(
    UTILIZATOR_ID number(4),
    NUME varchar2(20) NOT NULL,
    PRENUME varchar2(20) NOT NULL,
    EMAIL varchar2(50) NOT NULL,
    TELEFON varchar2(10),
    
    CONSTRAINT pk_utilizator PRIMARY KEY (UTILIZATOR_ID),
    CONSTRAINT uq_utilizator_email UNIQUE (EMAIL),
    CONSTRAINT ck_utilizator_telefon 
        CHECK (TELEFON LIKE '07%' and length(TELEFON) = 10),
    CONSTRAINT ck_utilizator_email_format 
        CHECK (EMAIL LIKE '%@%.%' 
            and length(EMAIL) - length(replace(EMAIL, '@', '')) = 1)
);

create table COMANDA(
    COMANDA_ID number(10),
    DATA_COMANDA DATE DEFAULT SYSDATE NOT NULL,
    UTILIZATOR_ID NUMBER(4) NOT NULL,
    
    CONSTRAINT pk_comanda PRIMARY KEY (COMANDA_ID),
    CONSTRAINT fk_comanda_utilizator FOREIGN KEY (UTILIZATOR_ID) 
        REFERENCES UTILIZATOR(UTILIZATOR_ID)
        ON DELETE CASCADE
);


create table PRODUCATOR(
    PRODUCATOR_ID number(4),
    NUME VARCHAR2(50) NOT NULL,
    TARA_ORIGINE VARCHAR2(50) NOT NULL,
    GARANTIE_STANDARD NUMBER(3) NOT NULL,
    
    CONSTRAINT pk_producator PRIMARY KEY (PRODUCATOR_ID),
    CONSTRAINT ck_tara_majuscula 
        CHECK (SUBSTR(TARA_ORIGINE, 1, 1) = UPPER(SUBSTR(TARA_ORIGINE, 1, 1))),
    CONSTRAINT ck_garantie_standard
        CHECK (GARANTIE_STANDARD >= 24)
);

create table PRODUS(
    PRODUS_ID varchar2(10), 
    NUME varchar2(40) NOT NULL,
    TIP_CONTACT varchar2(8) NOT NULL,
    PRET number(6, 2) NOT NULL,
    FRECVENTA_MINIMA number(5) NOT NULL,
    FRECVENTA_MAXIMA number(5) NOT NULL,
    WIRELESS varchar2(2) NOT NULL,
    PRODUCATOR_ID number(4) NOT NULL,
    
    CONSTRAINT pk_produs PRIMARY KEY (PRODUS_ID),
    CONSTRAINT fk_produs_producator FOREIGN KEY (PRODUCATOR_ID) 
        REFERENCES PRODUCATOR(PRODUCATOR_ID),
    CONSTRAINT ck_produs_tip_contact 
        CHECK (TIP_CONTACT IN ('In-Ear', 'Over-Ear')),
    CONSTRAINT ck_produs_wireless 
        CHECK (WIRELESS IN ('DA', 'NU')),
    CONSTRAINT ck_pret
        CHECK (pret > 0),
    CONSTRAINT ck_produs_frecventa_minima 
        CHECK (FRECVENTA_MINIMA > 0),
    CONSTRAINT ck_produs_interval_frecventa 
        CHECK (FRECVENTA_MAXIMA > FRECVENTA_MINIMA)
);

create table DISTRIBUITOR(
    DISTRIBUITOR_ID number(4),
    NUME varchar2(50) NOT NULL,
    COD_UNIC_INREGISTRARE VARCHAR2(10) NOT NULL,
    TIP_ENTITATE varchar2(3) NOT NULL,
    
    CONSTRAINT pk_distribuitor PRIMARY KEY (DISTRIBUITOR_ID),
    CONSTRAINT ck_cui_lungime
        CHECK (LENGTH(COD_UNIC_INREGISTRARE) >= 6),
    CONSTRAINT uq_dist_cui UNIQUE (COD_UNIC_INREGISTRARE),
    CONSTRAINT ck_tip_entitate CHECK (TIP_ENTITATE IN ('SRL', 'SA'))
);

create table DEPOZIT(
    DEPOZIT_ID number(4),
    NUME varchar2(50) NOT NULL,
    JUDET varchar2(30) NOT NULL,
    ORAS varchar2(30) NOT NULL,
    STRADA varchar2(40)NOT NULL,
    NUMAR_STRADA varchar2(5) NOT NULL,
    DISTRIBUITOR_ID number(4) NOT NULL,
    
    CONSTRAINT pk_depozit PRIMARY KEY (DEPOZIT_ID),
    CONSTRAINT fk_depozit_distribuitor FOREIGN KEY (DISTRIBUITOR_ID) 
        REFERENCES DISTRIBUITOR(DISTRIBUITOR_ID)
);

create table INVENTAR(
    DEPOZIT_ID number(4),
    PRODUS_ID varchar2(10),
    
    CONSTRAINT pk_inventar PRIMARY KEY (DEPOZIT_ID, PRODUS_ID),
    CONSTRAINT fk_inventar_depozit FOREIGN KEY (DEPOZIT_ID) 
        REFERENCES DEPOZIT(DEPOZIT_ID),
    CONSTRAINT fk_inventar_produs FOREIGN KEY (PRODUS_ID) 
        REFERENCES PRODUS(PRODUS_ID)
);

create table ELEMENTE_COMANDA(
    COMANDA_ID number(10),
    DEPOZIT_ID number(4),
    PRODUS_ID varchar2(10),
    CONSTRAINT pk_elem_comanda PRIMARY KEY (COMANDA_ID, DEPOZIT_ID, PRODUS_ID),
    CONSTRAINT fk_elem_comanda_id FOREIGN KEY (COMANDA_ID) 
        REFERENCES COMANDA(COMANDA_ID)
        ON DELETE CASCADE,
    CONSTRAINT fk_elem_inventar FOREIGN KEY (DEPOZIT_ID, PRODUS_ID) 
        REFERENCES INVENTAR(DEPOZIT_ID, PRODUS_ID)
);



